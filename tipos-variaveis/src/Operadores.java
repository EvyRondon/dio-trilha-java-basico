public class Operadores {
  public static void main(String[] args) throws Exception {
    int numero = 5;

    // faz a subtração depois
    System.out.println(numero--);

    System.out.println(numero);

    String meuNome = "Evelym";
    String meuNome1 = new String("Evelym");
    // usamos o equals para comparar conteudo
    System.out.println(meuNome.equals(meuNome1));

  }

  public long GetPositiveNumber(int numero) {
    return numero = numero * -1;
  }
}
